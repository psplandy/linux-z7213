static s32 LCD_esd_check(u32 sel)
{
	unsigned char buffer[4];
	__u32 rx_num = 0;

	if (ic_type == IC_SC7705) {
		/*
		 * SC7705 is suggested to check 09h=0x80 and BAh=BA_P1 instead of
		 * 0Ah=0x9C
		 */
		dsi_close_panel(sel);
		dsi_dcs_rd(sel, 0x09, &buffer[0], &rx_num);
		dsi_dcs_rd(sel, 0xBA, &buffer[1], &rx_num);
		dsi_open_panel(sel);

		if (buffer[0] != 0x80 || buffer[1] != REG_BA_P1) {
			printk("LCD reg 09h=%02x BAh=%02x\n", buffer[0], buffer[1]);
			return true;
		}
	} else if (ic_type == IC_ILI9881C) {
		dsi_close_panel(sel);
		dsi_dcs_rd(sel, 0x0A, &buffer[0], &rx_num);
		dsi_open_panel(sel);

		if (buffer[0] != 0x9C) {
			printk("LCD reg 0Ah=%02x\n", buffer[0]);
			return true;
		}
	}

	return false;
}

static s32 LCD_reset_panel(u32 sel)
{
	sunxi_lcd_delay_ms(10);
	LCD_panel_exit(sel);
	sunxi_lcd_delay_ms(10);
	LCD_power_off(sel);
	sunxi_lcd_delay_ms(10);
	LCD_power_on(sel);
	sunxi_lcd_delay_ms(120);
	LCD_panel_init(sel);
}

